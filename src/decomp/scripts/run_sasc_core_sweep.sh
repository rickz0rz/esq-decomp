OUT_DIR="build/decomp/sasc_trial"
mkdir -p "$OUT_DIR"

failed=0
echo "SAS/C core sweep (strict=${STRICT})"

matches_filter() {
    local path="$1"
    if [ "${#FILTERS[@]}" -eq 0 ]; then
        return 0
    fi
    local filt
    for filt in "${FILTERS[@]}"; do
        case "$path" in
            *"$filt"*)
                return 0
                ;;
        esac
    done
    return 1
}

ran_count=0

for script in "${SCRIPTS[@]}"; do
    if ! matches_filter "$script"; then
        continue
    fi

    if [ ! -f "$script" ]; then
        echo "skip (missing): $script"
        continue
    fi

    ran_count=$((ran_count + 1))
    echo "run: $script"
    bash "$script" >/tmp/run_sasc_core_sweep.log 2>&1 || {
        echo "error: compare script failed: $script"
        cat /tmp/run_sasc_core_sweep.log
        failed=1
        continue
    }

    base="$(basename "$script" .sh)"
    base="${base#compare_sasc_}"
    base="${base%_trial}"
    diff_file="${OUT_DIR}/${base}.semantic.diff"

    if [ ! -f "$diff_file" ]; then
        echo "warn: semantic diff not found for $script (expected $diff_file)"
        failed=1
        continue
    fi

    size="$(wc -c <"$diff_file" | tr -d ' ')"
    echo "  semantic: ${diff_file} (${size} bytes)"
    if [ "$size" -ne 0 ]; then
        failed=1
    fi
done

if [ "${#FILTERS[@]}" -gt 0 ]; then
    echo "SAS/C core sweep: selected ${ran_count} lane(s) via --filter"
fi

if [ "$CLEAN_GENERATED_DIS" -eq 1 ]; then
    cleaned=0
    while IFS= read -r dis_file; do
        [ -z "$dis_file" ] && continue
        if git ls-files --error-unmatch "$dis_file" >/dev/null 2>&1; then
            continue
        fi
        rm -f "$dis_file"
        cleaned=$((cleaned + 1))
    done < <(find src/decomp/sas_c -maxdepth 1 -type f -name '*.dis' | sort)
    echo "SAS/C core sweep: cleaned ${cleaned} untracked generated .dis file(s)"
fi

if [ "$failed" -ne 0 ]; then
    echo "SAS/C core sweep: semantic mismatches present"
    if [ "$STRICT" -eq 1 ]; then
        exit 1
    fi
else
    echo "SAS/C core sweep: all semantic diffs are zero"
fi
