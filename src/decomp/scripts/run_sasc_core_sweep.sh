}

while [ $# -gt 0 ]; do
    case "$1" in
        --strict)
            STRICT=1
            shift
            ;;
        --filter)
            if [ $# -lt 2 ]; then
                echo "error: --filter requires a substring argument" >&2
                usage
                exit 2
            fi
            FILTERS+=("$2")
            shift 2
            ;;
        --clean-generated-dis)
            CLEAN_GENERATED_DIS=1
            shift
            ;;
        -h|--help)
            usage
            exit 0
            ;;
