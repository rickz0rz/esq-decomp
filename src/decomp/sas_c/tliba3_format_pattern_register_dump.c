extern void FORMAT_RawDoFmtWithScratchBuffer(const char *fmt, ...);

static ULONG join_ptr_words(UWORD hi, UWORD lo)
{
    return ((ULONG)hi << PTR_HI_SHIFT) | (ULONG)lo;
}

void TLIBA3_FormatPatternRegisterDump(const char *title, const UWORD *regs)
{
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_PCT_S_COLON_DIWOFFSET_PCT_04LX_DDFOF,
        title,
        (ULONG)TLIBA1_DiagDiwOffset,
        (ULONG)TLIBA1_DiagDdfOffset,
        (ULONG)TLIBA1_DiagBplcon1Value);

    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_DIWSTRT_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_DIWSTRT_ADDR_INDEX],
        (ULONG)regs[REG_DIWSTRT_VALUE_INDEX],
        (ULONG)regs[REG_DIWSTRT_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_DIWSTOP_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_DIWSTOP_ADDR_INDEX],
        (ULONG)regs[REG_DIWSTOP_VALUE_INDEX],
        (ULONG)regs[REG_DIWSTOP_VALUE_INDEX] + DIWSTOP_EXTEND_ADDEND);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_DDFSTRT_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_DDFSTRT_ADDR_INDEX],
        (ULONG)regs[REG_DDFSTRT_VALUE_INDEX],
        (ULONG)regs[REG_DDFSTRT_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_DDFSTOP_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_DDFSTOP_ADDR_INDEX],
        (ULONG)regs[REG_DDFSTOP_VALUE_INDEX],
        (ULONG)regs[REG_DDFSTOP_VALUE_INDEX]);

    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL1MOD_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL1MOD_ADDR_INDEX],
        (ULONG)regs[REG_BPL1MOD_VALUE_INDEX],
        (ULONG)regs[REG_BPL1MOD_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL2MOD_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL2MOD_ADDR_INDEX],
        (ULONG)regs[REG_BPL2MOD_VALUE_INDEX],
        (ULONG)regs[REG_BPL2MOD_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPLCON0_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPLCON0_ADDR_INDEX],
        (ULONG)regs[REG_BPLCON0_VALUE_INDEX],
        (ULONG)regs[REG_BPLCON0_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPLCON1_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPLCON1_ADDR_INDEX],
        (ULONG)regs[REG_BPLCON1_VALUE_INDEX],
        (ULONG)regs[REG_BPLCON1_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPLCON2_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPLCON2_ADDR_INDEX],
        (ULONG)regs[REG_BPLCON2_VALUE_INDEX],
        (ULONG)regs[REG_BPLCON2_VALUE_INDEX]);

    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL1PTH_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL1PTH_ADDR_INDEX],
        (ULONG)regs[REG_BPL1PTH_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL1PTL_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL1PTL_ADDR_INDEX],
        (ULONG)regs[REG_BPL1PTL_VALUE_INDEX],
        join_ptr_words(regs[REG_BPL1PTH_VALUE_INDEX], regs[REG_BPL1PTL_VALUE_INDEX]));
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL2PTH_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL2PTH_ADDR_INDEX],
        (ULONG)regs[REG_BPL2PTH_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL2PTL_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL2PTL_ADDR_INDEX],
        (ULONG)regs[REG_BPL2PTL_VALUE_INDEX],
        join_ptr_words(regs[REG_BPL2PTH_VALUE_INDEX], regs[REG_BPL2PTL_VALUE_INDEX]));
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL3PTH_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL3PTH_ADDR_INDEX],
        (ULONG)regs[REG_BPL3PTH_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL3PTL_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL3PTL_ADDR_INDEX],
        (ULONG)regs[REG_BPL3PTL_VALUE_INDEX],
        join_ptr_words(regs[REG_BPL3PTH_VALUE_INDEX], regs[REG_BPL3PTL_VALUE_INDEX]));
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL4PTH_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL4PTH_ADDR_INDEX],
        (ULONG)regs[REG_BPL4PTH_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL4PTL_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL4PTL_ADDR_INDEX],
        (ULONG)regs[REG_BPL4PTL_VALUE_INDEX],
        join_ptr_words(regs[REG_BPL4PTH_VALUE_INDEX], regs[REG_BPL4PTL_VALUE_INDEX]));
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL5PTH_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL5PTH_ADDR_INDEX],
        (ULONG)regs[REG_BPL5PTH_VALUE_INDEX]);
    FORMAT_RawDoFmtWithScratchBuffer(
        TLIBA1_FMT_BPL5PTL_COLON_0X_PCT_04LX_0X_PCT_04L,
        (ULONG)regs[REG_BPL5PTL_ADDR_INDEX],
        (ULONG)regs[REG_BPL5PTL_VALUE_INDEX],
        join_ptr_words(regs[REG_BPL5PTH_VALUE_INDEX], regs[REG_BPL5PTL_VALUE_INDEX]));

    FORMAT_RawDoFmtWithScratchBuffer(TLIBA1_STR_PatternDumpSeparatorNewline);
}
